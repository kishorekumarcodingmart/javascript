// Operator	    Description	    Example
// &&	        and	            (x < 10 && y > 1) is true	
// ||	        or	            (x == 5 || y == 5) is false	
// !	        not	            !(x == y) is true

let x = 6
let y = 3

console.log(x < 10 && y > 1) //true

console.log(x == 5 || y == 5) //false

console.log(!(x == y)) //true