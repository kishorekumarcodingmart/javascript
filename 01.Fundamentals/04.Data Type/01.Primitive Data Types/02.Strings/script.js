let Name = "Kishore Kumar"
Name = 'M R Kishore Kumar'

console.log(Name) //M R Kishore Kumar


// String Length
console.log(Name.length) //17

// String Indexing
// No Negative Indexing
// Starting Indexing
console.log(Name[0]) //M
// Last Indexing
console.log(Name[Name.length-1]) //r


